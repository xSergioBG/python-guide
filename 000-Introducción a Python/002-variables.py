# variables.py

"""
Este programa muestra ejemplos de declaración y uso de variables en Python.
"""

def main():
    """
    Función principal del programa.
    """
    # Variables numéricas
    edad = 25
    altura = 1.75

    # Variables de texto
    nombre = "Juan"
    apellido = "Pérez"

    # Variables booleanas
    es_estudiante = True
    tiene_trabajo = False

    # Imprimir el contenido de las variables
    print("Edad:", edad)
    print("Altura:", altura)
    print("Nombre completo:", nombre, apellido)
    print("Es estudiante:", es_estudiante)
    print("Tiene trabajo:", tiene_trabajo)

if __name__ == "__main__":
    main()
